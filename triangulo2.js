//4. Dado os três lados de um triângulo determinar o perímetro do mesmo.
alert('Calculando o perímetro de um triângulo')
let lado1 = Number(prompt('Digite o valor do primeiro lado: '))
let lado2 = Number(prompt('Digite o valor do segundo lado: '))
let lado3 = Number(prompt('Digite o valor do terceiro lado: '))
let perimetro = lado1 + lado2 + lado3
alert("O perímetro do triângulo é igual a: " + perimetro + "m")